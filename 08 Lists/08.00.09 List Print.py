"""
In Python, you can display a list of strings using one-line commands. For that, the method join is used; this method has one parameter: a list of strings. It returns the string obtained by concatenation of the elements given, and the separator is inserted between the elements of the list; this separator is equal to the string on which is the method applied.
"""

